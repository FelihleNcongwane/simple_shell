/**
 * clear_info - initializes info_t struct
 * @info: struct address
 */
void clear_info(info_t *info)
{
	info->arg = NULL;
	info->argv = NULL;
	info->path = NULL;
	info->argc = 0;
}

/**
 * set_info - initializes info_t struct
 * @info: struct address
 * @av: argument vector
 */
void set_info(info_t *info, char **av)
{
	int i = 0;
	char **argv = NULL;

	info->fname = av[0];
	if (info->arg)
	{
		argv = strtow(info->arg, " \t");
		if (!argv)
		{
			argv = malloc(sizeof(char *) * 2);
			if (argv)
			{
				argv[0] = _strdup(info->arg);
				argv[1] = NULL;
			}
		}
		i = 0;
		while (argv && argv[i])
			i++;
		info->argc = i;

		switch_alias(info);
		switch_vars(info);
	}

	info->argv = argv;
}

/**
 * free_info - frees info_t struct fields
 * @info: struct address
 * @all: true if freeing all fields
 */
void free_info(info_t *info, int all)
{
	ffree(info->argv);
	info->argv = NULL;
	info->path = NULL;
	if (all)
	{
		switch (info->cmd_buf_type)
		{
			case CMD_BUF_NULL:
				free(info->arg);
				break;
			case CMD_BUF_MALLOC:
				free(*info->cmd_buf);
				break;
			default:
				break;
			}
		if (info->env)
			free_list(&(info->env));
		if (info->history)
			free_list(&(info->history));
		if (info->alias)
			free_list(&(info->alias));
		ffree(info->environ);
		info->environ = NULL;
		bfree((void **)info->cmd_buf);
		if (info->readfd > 2)
			close(info->readfd);
		_putchar(BUF_FLUSH);
	}
}
